function EmojiPicker({ onEmojiSelect, onClose }) {
  const [activeCategory, setActiveCategory] = useState('Smileys')

  const handleEmojiClick = (emoji) => {
    onEmojiSelect(emoji)
  }

  return (
    <div className="emoji-picker-overlay" onClick={onClose}>
      <div className="emoji-picker" onClick={(e) => e.stopPropagation()}>
        <div className="emoji-picker-header">
          <span className="emoji-picker-title">Pick an emoji</span>
          <button className="emoji-close-btn" onClick={onClose}>Ã—</button>
        </div>
        
        <div className="emoji-categories">
          {Object.keys(EMOJI_CATEGORIES).map(category => (
            <button
              key={category}
              className={`emoji-category-btn ${activeCategory === category ? 'active' : ''}`}
              onClick={() => setActiveCategory(category)}
            >
              {category}
            </button>
          ))}
        </div>

        <div className="emoji-grid">
          {EMOJI_CATEGORIES[activeCategory].map((emoji, index) => (
            <button
              key={index}
              className="emoji-btn"
              onClick={() => handleEmojiClick(emoji)}
            >
              {emoji}
            </button>
          ))}
        </div>
      </div>
    </div>
  )
}

